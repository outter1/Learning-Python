day = int(input("Digite um dia de 1 a 7: "))
if day == 1:
    print("Domingo")

elif day == 2:
    print("Segunda")

elif day == 3: 
    print("Terça")

elif day == 4: 
    print("Quarta")

elif day == 5: 
    print("Quinta")

elif day == 6:
    print("Sexta")

elif day == 7:
    print("Sábado")

else:
    print("Não existe esse dia.")