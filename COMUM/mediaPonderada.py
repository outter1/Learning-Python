# Apresentação das notas
print("Bem vinde jovem padawen.")
print("A seguir você irá descobrir a média das suas notas.")
notaUm = float(input("Digite a primeira nota: "))
notaDois = float(input("Digite a segunda nota: "))
notaTres = float(input("Digite a terceira nota: "))
notaQuatro = float(input("Digite a Quarta nota: "))
media = ((notaUm * 1) + (notaDois * 2) + (notaTres * 3) + (notaQuatro * 4)) / 10

# Mostrar a média
print(f"A média é: {media}")
