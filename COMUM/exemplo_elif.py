nota = float(input("Digite a nota do aluno: "))

if nota >= 9.0:
    print("Aluno com a nota A")
    
elif nota >= 8.0:
    print("Aluno com a nota B")
    
elif nota >= 7.0:
    print("Aluno com a nota C")
    
elif nota >= 6.0:
    print("Aluno com a nota D")
    
else:
    print("Aluno com a nota F")
    